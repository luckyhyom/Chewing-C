#include <stdio.h>

/*
    컴퓨터에서 데이터는 0과 1 뿐이다.
    'a'를 문자로 볼것인지, 숫자로 볼것인지 정해야한다.
    컴퓨터에서 'a'를 문자로 보면 'a'고, 숫자로 보면 97이다. (char 자료형을 사용했을때 이 과정이 이뤄지겠지?)
    
    1. char는 1 byte 크기를 차지하는 자료형일 뿐이다.
    2. ASCII 코드의 번호도 결국 이진수로 변환되어 메모리에 저장된다.
    3. 쌍따옴표("")는 문자열을 묶어서 메모리에 연속적으로 저장한다.

    4. a에 대한 그래픽 표현은 폰트 렌더링 엔진에 의해 처리된다.
    폰트: 각 문자에 대응하는 그래픽적 형태를 정의한 데이터. (TTF, OTF, 등의 글꼴 파일)

    결국!!
    ASCII Code는 그냥 사람이 보기 편하게 만든 표일 뿐이다.
    컴퓨터는 2진수로 128까지의 데이터를 가지고 있고, 폰트 파일에 해당 번호에 대응하는 그래픽 데이터가 존재한다.
    따라서, 프로그램을 작성할때는 한 글자당 1바이트의 메모리를 차지하지만, 프로그램을 실행시키는 컴퓨터에서는 폰트에 따라 더 많은 메모리를 사용할 수도 있다.

    ----

    텍스트를 화면에 출력하려면, 텍스트 데이터(문자 자체를 나타내는 데이터)와 그래픽 데이터(문자의 시각적 표현을 위한 데이터)가 필요합니다.
    전체 메모리 사용량은 이 두 가지 데이터를 합한 값으로, 화면에 문자를 표시하는 데 필요한 메모리의 총량을 나타냅니다.

    만약 'a'라는 문자를 ASCII 인코딩으로 저장하고, 이를 16x16 비트맵 폰트로 화면에 그린다면:
        텍스트 데이터: 'a' = 1바이트 (ASCII 코드 97)
        그래픽 데이터: 16x16 픽셀 = 32바이트 (각 픽셀이 1비트로 표현된다고 가정)
    따라서, 총 메모리 사용량은 1바이트 + 32바이트 = 33바이트가 됩니다.

    ----
    Tip.
    1. 현대의 모든 컴퓨터에는 여러가지 인코딩 방식이 기본적으로 설치되어있다.
    2. 폰트 파일이 필수적으로 설치되어있다.
    
    이 두가지를 이용하여 문자를 표현한다.
*/

int main() {
    char a = 'a';
    printf("a는 숫자로 %d이며 a는 글자로 %c이다. \n", a, a);
    return 0;
}